describe("PostgreSQL: GENERATED AS IDENTITY", () => {
  it("INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY → INTEGER PRIMARY KEY AUTOINCREMENT", () => {
    const sql = "CREATE TABLE users (id INT GENERATED ALWAYS AS IDENTITY PRIMARY KEY, name TEXT);";
    const result = postgresToSqlite(sql);
    expect(result).toBe("CREATE TABLE users (id INTEGER PRIMARY KEY AUTOINCREMENT, name TEXT);");
  });

  it("INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY → INTEGER PRIMARY KEY AUTOINCREMENT", () => {
    const sql = "CREATE TABLE orders (id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY, total REAL);";
    const result = postgresToSqlite(sql);
    expect(result).toBe("CREATE TABLE orders (id INTEGER PRIMARY KEY AUTOINCREMENT, total REAL);");
  });

  it("INT GENERATED ALWAYS AS IDENTITY (no PK) → INTEGER", () => {
    const sql = "CREATE TABLE logs (id INT GENERATED ALWAYS AS IDENTITY, message TEXT);";
    const result = postgresToSqlite(sql);
    expect(result).toContain("id INTEGER");
    expect(result).not.toContain("AUTOINCREMENT");
  });
});

