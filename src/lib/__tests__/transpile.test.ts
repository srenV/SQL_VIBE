describe("PostgreSQL: DEFAULT CURRENT_TIMESTAMP protection", () => {
  it("DEFAULT CURRENT_TIMESTAMP is preserved (not converted to DATETIME('now'))", () => {
    const sql = "CREATE TABLE logs (id INTEGER PRIMARY KEY, created_at TEXT DEFAULT CURRENT_TIMESTAMP);";
    const result = postgresToSqlite(sql);
    expect(result).toContain("DEFAULT CURRENT_TIMESTAMP");
    expect(result).not.toContain("DEFAULT DATETIME('now')");
  });

  it("DEFAULT CURRENT_TIMESTAMP() is preserved", () => {
    const sql = "CREATE TABLE logs (id INTEGER PRIMARY KEY, created_at TEXT DEFAULT CURRENT_TIMESTAMP());";
    const result = postgresToSqlite(sql);
    expect(result).toContain("DEFAULT CURRENT_TIMESTAMP");
    expect(result).not.toContain("DEFAULT DATETIME('now')");
  });

  it("NOW() in SELECT is still converted to DATETIME('now')", () => {
    const sql = "SELECT NOW(), created_at FROM logs;";
    const result = postgresToSqlite(sql);
    expect(result).toContain("DATETIME('now')");
  });

  it("CURRENT_TIMESTAMP in WHERE clause is converted to DATETIME('now')", () => {
    const sql = "SELECT * FROM logs WHERE created_at > CURRENT_TIMESTAMP;";
    const result = postgresToSqlite(sql);
    expect(result).toContain("DATETIME('now')");
  });

  it("Mixed: DEFAULT CURRENT_TIMESTAMP preserved + NOW() converted", () => {
    const sql = "CREATE TABLE logs (id INTEGER PRIMARY KEY, created_at TEXT DEFAULT CURRENT_TIMESTAMP, count INTEGER DEFAULT 0);";
    const result = postgresToSqlite(sql);
    expect(result).toContain("DEFAULT CURRENT_TIMESTAMP");
    expect(result).toContain("DEFAULT 0");
  });

  it("TIMESTAMP DEFAULT CURRENT_TIMESTAMP → TEXT DEFAULT CURRENT_TIMESTAMP", () => {
    const sql = "CREATE TABLE users (id SERIAL PRIMARY KEY, name VARCHAR(100), created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP);";
    const result = postgresToSqlite(sql);
    expect(result).toContain("name TEXT");
    expect(result).toContain("created_at TEXT DEFAULT CURRENT_TIMESTAMP");
    expect(result).not.toContain("DATETIME('now')");
  });
});

